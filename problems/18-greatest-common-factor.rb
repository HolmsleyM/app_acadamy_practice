# integer that evenly divides both numbers. You may wish to use the
# `%` modulo operation.
#
# Difficulty: medium.

def greatest_common_factor(number1, number2)
 
  greatest_factor = nil
  x = 0
until x == number1 || x == number2
  common_factor = x + 1  
  if number1 % common_factor == 0 && number2 % common_factor == 0
     greatest_factor = common_factor
  end
  x += 1 
end 
  puts greatest_factor
  greatest_factor
end

